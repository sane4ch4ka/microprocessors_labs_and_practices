#include <8051.h>

// ??????? ????????
void delay(unsigned int count) {
    unsigned int i;
    for (i = 0; i < count; i++);
}

// ???????? ???????
void lcd_cmd(unsigned char cmd) {
    P0 = cmd;                    
    P2 = P2 & 0xF9;              // RS=0, RW=0
    P2 = P2 | 0x01;              // E=1
    P2 = P2 & 0xFE;              // E=0
}

// ???????? ??????
void lcd_data(unsigned char dat) {
    P0 = dat;                    
    P2 = P2 | 0x02;              // RS=1
    P2 = P2 & 0xFB;              // RW=0
    P2 = P2 | 0x01;              // E=1
    P2 = P2 & 0xFE;              // E=0
}

// ????????????? LCD
void lcd_init(void) {
    delay(100);                  
    lcd_cmd(0x38);               
    lcd_cmd(0x0E);               
    lcd_cmd(0x06);               
    lcd_cmd(0x01);               
    delay(50);                   
}

// ??????? ???????
void main(void) {
    unsigned int i, j;
    
    // ??????? ????? "???????" (????????)
    unsigned char word1[] = "sanechka";
    unsigned char word2[] = "SaNe4kA";
    
    lcd_init();
    
    // ????? ??????? ????? ? 8-? ???????
    lcd_cmd(0x87);                // 8-? ??????? ?????? ??????
    for(i = 0; i < 8; i++) {
        lcd_data(word1[i]);
    }
    
    // ??????? ?????? ????? (? ??????? ????)
    while(1) {
        for(j = 0; j < 16; j++) {
            lcd_cmd(0xC0);        // ?????? ?????? ??????
            
            for(i = 0; i < 16; i++) {
                if(i >= 15 - j && i < 15 - j + 8) {
                    lcd_data(word2[i - (15 - j)]);
                } else {
                    lcd_data(' ');
                }
            }
            delay(5);
        }
    }
}

