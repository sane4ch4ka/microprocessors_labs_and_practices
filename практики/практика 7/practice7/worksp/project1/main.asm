$MOD52

ORG 0000h
LJMP START

ORG 0003h
LJMP INT0_ISR

ORG 000Bh
LJMP TIMER0_ISR

ORG 0013h
LJMP INT1_ISR

ORG 001Bh
LJMP TIMER1_ISR

ORG 100H

DUTY        EQU 30H
HIGH_TIME_L EQU 31H
HIGH_TIME_H EQU 32H

PERIOD_H    EQU 0BH
PERIOD_L    EQU 0DCH

START:
    MOV SP,  #07H
    MOV P1,  #0FFH
    MOV P3,  #0FFH
    CLR P1.0

    MOV DUTY, #45

    MOV A, DUTY
    LCALL SET_HIGH_TIME

    MOV TMOD, #11H
    MOV TH1,  #PERIOD_H
    MOV TL1,  #PERIOD_L
    MOV TH0,  #00H
    MOV TL0,  #00H

    SETB IT0
    SETB IT1
    SETB EX0
    SETB EX1
    SETB ET1
    SETB ET0
    SETB EA
    SETB TR1

MAIN:
    SJMP MAIN

INT0_ISR:
    PUSH ACC
    PUSH PSW
    MOV R7, #20
DLY1:
    MOV R6, #50
DLY2:
    DJNZ R6, DLY2
    DJNZ R7, DLY1
    JB P3.2, EXIT_UP
    MOV A, DUTY
    CJNE A, #75, INC_DUTY
    SJMP EXIT_UP
INC_DUTY:
    ADD A, #5
    MOV DUTY, A
    MOV A, DUTY
    LCALL SET_HIGH_TIME
EXIT_UP:
    POP PSW
    POP ACC
    RETI

INT1_ISR:
    PUSH ACC
    PUSH PSW
    MOV R7, #20
DLY3:
    MOV R6, #50
DLY4:
    DJNZ R6, DLY4
    DJNZ R7, DLY3
    JB P3.3, EXIT_DOWN
    MOV A, DUTY
    CJNE A, #45, DEC_DUTY
    SJMP EXIT_DOWN
DEC_DUTY:
    CLR C
    SUBB A, #5
    MOV DUTY, A
    MOV A, DUTY
    LCALL SET_HIGH_TIME
EXIT_DOWN:
    POP PSW
    POP ACC
    RETI

SET_HIGH_TIME:
    CJNE A, #45, C50
    MOV HIGH_TIME_H, #92H
    MOV HIGH_TIME_L, #23H
    RET
C50:
    CJNE A, #50, C55
    MOV HIGH_TIME_H, #86H
    MOV HIGH_TIME_L, #0EH
    RET
C55:
    CJNE A, #55, C60
    MOV HIGH_TIME_H, #79H
    MOV HIGH_TIME_L, #0B9H
    RET
C60:
    CJNE A, #60, C65
    MOV HIGH_TIME_H, #6DH
    MOV HIGH_TIME_L, #84H
    RET
C65:
    CJNE A, #65, C70
    MOV HIGH_TIME_H, #61H
    MOV HIGH_TIME_L, #4FH
    RET
C70:
    CJNE A, #70, C75
    MOV HIGH_TIME_H, #55H
    MOV HIGH_TIME_L, #1AH
    RET
C75:
    CJNE A, #75, DEF
    MOV HIGH_TIME_H, #48H
    MOV HIGH_TIME_L, #0E5H
    RET
DEF:
    MOV HIGH_TIME_H, #92H
    MOV HIGH_TIME_L, #23H
    RET

TIMER1_ISR:
    CLR  TF1
    MOV  TH1, #PERIOD_H
    MOV  TL1, #PERIOD_L
    CLR  TR0
    CLR  P1.0
    MOV  TH0, HIGH_TIME_H
    MOV  TL0, HIGH_TIME_L
    SETB P1.0
    SETB TR0
    RETI

TIMER0_ISR:
    CLR TF0
    CLR P1.0
    CLR TR0
    RETI

END

